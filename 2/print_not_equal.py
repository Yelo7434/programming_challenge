# Create a program that asks the user to input 2 numbers. Print a message if they are not equal.
# Get user input two numbers.
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
# Compare the two numbers and print a message if they are not equal.
if num1 != num2:
    print("The numbers are not equal.")
else:    
    print("The numbers are equal.")
