# Create a program that ask user to input 2 numbers. Print the quotient of the two numbers without the decimal point
# Get user input two numbers.
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
# Calculate the quotient of the two numbers and print the result without the decimal point.
quotient = num1 // num2
print("The quotient of the two numbers is:", quotient)